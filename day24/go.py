def checkfast(inputstr):
    digits = [int(c) for c in inputstr]
    if digits[0] != digits[13]:
        return False
    if digits[1] - 7 != digits[12]:
        return False
    if digits[2] + 5 != digits[11]:
        return False
    if digits[3] + 7 != digits[6]:
        return False
    if digits[4] - 2 != digits[5]:
        return False
    if digits[7] + 4 != digits[8]:
        return False
    if digits[9] - 6 != digits[10]:
        return False
    return True

